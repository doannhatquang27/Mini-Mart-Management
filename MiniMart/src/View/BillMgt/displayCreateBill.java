    private void btnAddToCartMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddToCartMouseClicked
        int rowSelected = tblProduct.getSelectedRow();
        Cart cart = new Cart();
        cart.setProductId(Integer.valueOf((String)tblProduct.getValueAt(rowSelected, 0)));
        cart.setProductName((String) tblProduct.getValueAt(rowSelected, 1));
        cart.setPrice(Integer.parseInt(txtPrice.getText()));
        cart.setAmount((int) snrAmount.getValue());
        cartList.add(cart);
        
        txtPrice.setText("");
        snrAmount.setValue(0);
    }//GEN-LAST:event_btnAddToCartMouseClicked


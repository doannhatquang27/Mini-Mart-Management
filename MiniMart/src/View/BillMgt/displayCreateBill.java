    private void btnCartMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCartMouseClicked
        DefaultTableModel empModel = new DefaultTableModel();
        Vector<String> header = new Vector<>();
        Vector data = new Vector();
        
        header.add("Product ID");
        header.add("Product Name");
        header.add("Price");
        header.add("Amount");
        header.add("Total Price");
            
        for (int i = 0; i < cartList.size(); i++) {
            Vector<String> v = new Vector<>();
            v.add(Integer.toString(cartList.get(i).getProductId()));
            v.add(cartList.get(i).getProductName());
            v.add(Integer.toString(cartList.get(i).getPrice()));
            v.add(Integer.toString(cartList.get(i).getAmount()));
            int total = cartList.get(i).getPrice() * cartList.get(i).getAmount();
            v.add(Integer.toString(total));
            data.add(v);
        }
            
        empModel.setDataVector(data, header);
        
        displayCart dc = new displayCart();
        dc.setVisible(true);
        dc.setTable(empModel);
        dc.setUser(this.user);
        dc.setCartList(this.cartList);
    }//GEN-LAST:event_btnCartMouseClicked


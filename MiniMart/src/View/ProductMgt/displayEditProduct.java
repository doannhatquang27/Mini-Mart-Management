    private void cbxLanguageItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbxLanguageItemStateChanged
        Locale loc;

        if (cbxLanguage.getSelectedItem().equals("VN")) {
            loc = new Locale("vi", "VN");
            changeLanguageTo(loc);
        } else {
            loc = new Locale("en", "US");
            changeLanguageTo(loc);
        }
    }//GEN-LAST:event_cbxLanguageItemStateChanged

    public void changeLanguageTo(Locale loc) {
        ResourceBundle rb = ResourceBundle.getBundle("pkgLanguage/MessageBundle", loc);
        lbledipro.setText(rb.getString("edipro"));
        lblproid.setText(rb.getString("proid"));
        lblnam.setText(rb.getString("nam"));
        lblpri.setText(rb.getString("pri"));
        lblcat.setText(rb.getString("cat"));
        lblamo.setText(rb.getString("amo"));
        lbledipro.setText(rb.getString("edipro"));
        btnSave.setText(rb.getString("sav"));
        btnCancel.setText(rb.getString("can"));
    }
    
    public void fillBlank(Product temp) {
        txtProduceID.setText(Integer.toString(temp.getProductId()).trim());
        txtProduceName.setText(temp.getProductName().trim());
        txtPrice.setText(Integer.toString(temp.getPrice()).trim());
        txtAmount.setText(Integer.toString(temp.getAmount()).trim());
        cbxCategory.setSelectedItem(cbxCategory.getItemAt(temp.getCategory() - 1));
    }
    

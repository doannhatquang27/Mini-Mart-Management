    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked
        TblProduct exc = new TblProduct();
        
        int productId = Integer.parseInt(txtProduceID.getText());
        String newProductName = txtProduceName.getText().trim();
        int newProductPrice = Integer.parseInt(txtPrice.getText());
        int newAmount = Integer.parseInt(txtAmount.getText());
        int newCategory = cbxCategory.getSelectedIndex() + 1;

        boolean isDone;
        try {
            isDone = exc.updateDB(productId, newProductName, newProductPrice, newCategory, newAmount);
            if (isDone) this.dispose();
        } catch (ClassNotFoundException | SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveMouseClicked


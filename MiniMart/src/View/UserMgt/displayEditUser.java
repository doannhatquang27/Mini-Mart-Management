    public void changeLanguageTo(Locale loc) {
        ResourceBundle rb = ResourceBundle.getBundle("pkgLanguage/MessageBundle", loc);
        lblediuse.setText(rb.getString("ediuse"));
        lbluseid.setText(rb.getString("useid"));
        lblusenam.setText(rb.getString("username"));
        lblgro.setText(rb.getString("group"));
        lblpas.setText(rb.getString("password"));
        lblnam.setText(rb.getString("nam"));
        btnSave.setText(rb.getString("sav"));
        btnCancel.setText(rb.getString("can"));
        rbtInventory.setText(rb.getString("is"));
        rbtSeller.setText(rb.getString("seller"));
    }
    
    public void fillBlank(User temp) {
        txtUserID.setText(Integer.toString(temp.getUserID()).trim());
        txtUserName.setText(temp.getUserName().trim());
        txtLoginName.setText(temp.getUserLoginName().trim());
        txtPassword.setText(temp.getPassword().trim());
        
        switch (temp.getGroup()) {
            case 1: {
                rbtSeller.setEnabled(false);
                rbtInventory.setEnabled(false);
                break;
            }
            case 2: {
                rbtSeller.setSelected(true);
                break;
            }
            case 3: {
                rbtInventory.setSelected(true);
                break;
            }
        } 
    }

    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked
        TblUser exc = new TblUser();

        int newUserID = Integer.parseInt(txtUserID.getText().trim());
        String newUserLoginName = txtLoginName.getText().trim();
        String newUserName = txtUserName.getText().trim();
        int groupOfUser;
        if (rbtSeller.isSelected()) groupOfUser = 2;
        else groupOfUser = 3;
        String newPassword = txtPassword.getText().trim();
        
        if (checkInventoryGroup() == -1 || checkInventoryGroup() == newUserID || groupOfUser == 2) {
            boolean isDone;
            try {
                isDone = exc.updateDB(newUserID, newUserName, groupOfUser, newUserLoginName, newPassword);
                if (isDone) {
                    this.dispose();
                }
            } catch (ClassNotFoundException ex) {
            }
        } else {
            JOptionPane.showMessageDialog(this, "Already have a inventory staff");
        }   
        
        
    }//GEN-LAST:event_btnSaveMouseClicked

    /**
     * Check if the inventory staff is already
     * @return 
     */
    public int checkInventoryGroup() {
        TblUser exc = new TblUser();
        ArrayList<User> userList =  exc.loadUser();
        for (int i = 0; i < userList.size(); i++) {
            if (userList.get(i).getGroup() == 3) {
                return userList.get(i).getUserID();
            }
        }
        return -1;
    }
    
    private void cbxLanguageItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbxLanguageItemStateChanged
        Locale loc;

        if (cbxLanguage.getSelectedItem().equals("VN")) {
            loc = new Locale("vi", "VN");
            changeLanguageTo(loc);
        } else {
            loc = new Locale("en", "US");
            changeLanguageTo(loc);
        }
    }//GEN-LAST:event_cbxLanguageItemStateChanged

